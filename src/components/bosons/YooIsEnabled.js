export default {
  computed: {
    $yooIsEnabled () {
      const localAttr = this.$attrs.isEnabled
      const localProp = this.$props.isEnabled
      const parent = this.$parent.$attrs.isEnabled

      if (localAttr !== undefined) {
        return localAttr
      }

      if (localProp !== undefined) {
        return localProp
      }

      if (parent !== undefined) {
        return parent
      }

      return true
    }
  }
}
