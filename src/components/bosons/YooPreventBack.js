import {
  isIOS,
  AndroidApplication
} from '@nativescript/core'

export default {
  methods: {
    $yooPreventBackOn (fn) {
      if (!isIOS) {
        AndroidApplication.on(
          AndroidApplication.activityBackPressedEvent,
          fn
        )
      }
    },
    $yooPreventBackOff () {
      if (!isIOS) {
        AndroidApplication.off(
          AndroidApplication.activityBackPressedEvent
        )
      }
    }
  }
}
